#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	int		size;
	char	*str;

	if (!s1 || !set)
		return (NULL);
	while (*s1 && ft_strchr(set, *s1))
		s1++;
	size = ft_strlen(s1);
	while (size != 0 && ft_strchr(set, s1[size]))
		size--;
	str = ft_substr(s1, 0, size + 1);
	return (str);
}
